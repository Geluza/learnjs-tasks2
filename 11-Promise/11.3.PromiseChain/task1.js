//Eсли ошибка произойдёт в f1, то она будет 
//обработана в .catch в этом примере:
promise
  .then(f1)
  .catch(f2);


//  но не в этом:
promise
  .then(f1, f2);
//так как ошибка передаётся по цепочке, но во втором примере 
//нет продолжения цепочки после f1. То есть .then передаёт 
//результат или ошибку следующему блоку .then/catch. 
//Так как в первом примере в цепочке далее имеется блок catch, 
//а во втором – нет, то ошибка в нём останется необработанной.